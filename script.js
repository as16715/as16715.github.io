// Tabbed Menu
function openMenu(evt, menuName) {
    // Hide all elements with class "menu"
    var i, x, tablinks;
    x = document.getElementsByClassName("menu");
    for (i = 0; i < x.length; i++) {
        x[i].style.display = "none";
    }

    // Remove the "w3-dark-grey" class from all elements with class "tablink"
    tablinks = document.getElementsByClassName("tablink");
    for (i = 0; i < x.length; i++) {
        tablinks[i].className = tablinks[i].className.replace(" w3-dark-grey", "");
    }

    // Display the menu with the specified menuName
    document.getElementById(menuName).style.display = "block";

    // Add the "w3-dark-grey" class to the first child element of the clicked element (highlighting the tab)
    evt.currentTarget.firstElementChild.className += " w3-dark-grey";
}

